package bir20.muzzle;

public class Muzzle {
    private final String color;
    private final int size;

    public Muzzle(String color, int size) {
        this.color = color;
        this.size = size;
    }

    public String getColor() {
        return color;
    }

    public int getSize() {
        return size;
    }

}
